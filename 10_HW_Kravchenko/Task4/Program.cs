int m = Convert.ToInt32(Console.ReadLine());
int n = Convert.ToInt32(Console.ReadLine());

int d = Convert.ToInt32(Console.ReadLine()); //всего драконов

int totalAffectedDragons = 0;

for (int i = 1; i <= d; i++)
{
    if (i % k == 0) totalAffectedDragons++;
    else if (i % l == 0) totalAffectedDragons++;
    else if (i % m == 0) totalAffectedDragons++;
    else if (i % n == 0) totalAffectedDragons++;
}

Console.WriteLine(totalAffectedDragons);