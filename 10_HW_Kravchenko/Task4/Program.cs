// Входные данные
// Входные данные содержат целые числа k, l, m, n и d, каждое число в отдельной строке (1 ≤ k, l, m, n ≤ 10, 1 ≤ d ≤ 105).


int k = Convert.ToInt32(Console.ReadLine());// (1 ≤ k, l, m, n ≤ 10, 1 ≤ d ≤ 105) - каждый пострадавший драконы
